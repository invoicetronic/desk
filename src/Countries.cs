    public static List<SelectListItem> GetList()
    {
        var isItalian = CultureInfo.CurrentUICulture.TwoLetterISOLanguageName == "it";

        return List
            .Select(kvp => new SelectListItem
            {
                Value = kvp.Key,
                Text = isItalian ? kvp.Value.It : kvp.Value.En,
                Selected = kvp.Key == "IT"
            })
            .OrderBy(x => x.Text, StringComparer.Create(CultureInfo.CurrentUICulture, false))
            .ToList();
    }
